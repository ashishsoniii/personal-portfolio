// Helper function to get SEO config for specific page/section
export const getSEOConfig = (page, subPage = null) => {
  if (subPage && seoConfig[page] && seoConfig[page][subPage]) {
    return { ...seoConfig.default, ...seoConfig[page][subPage] };
  }
  
  if (seoConfig[page]) {
    return { ...seoConfig.default, ...seoConfig[page] };
  }
  
  return seoConfig.default;
};

